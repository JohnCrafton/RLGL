extension String {

    static var Club: String { return "\u{9827}" }
    static var Heart: String { return "\u{9829}" }
    static var Spade: String { return "\u{9824}" }
    static var Diamond: String { return "\u{9830}" }

    static var ClubOpen: String { return "\u{9831}" }
    static var HeartOpen: String { return "\u{9825}" }
    static var SpadeOpen: String { return "\u{9828}" }
    static var DiamondOpen: String { return "\u{9826}" }
}
